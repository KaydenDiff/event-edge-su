<script>
export default {
  name: 'HelpView',
  data() {
    return {
      activeSection: null
    }
  },
  mounted() {
    // –ï—Å–ª–∏ –≤ URL –µ—Å—Ç—å section, –∞–∫—Ç–∏–≤–∏—Ä—É–µ–º —Å–æ–æ—Ç–≤–µ—Ç—Å—Ç–≤—É—é—â–∏–π —Ä–∞–∑–¥–µ–ª
    const section = this.$route.query.section;
    if (section) {
      this.activeSection = section;
      this.$nextTick(() => {
        const element = document.getElementById(section);
        if (element) {
          element.scrollIntoView({ behavior: 'smooth', block: 'start' });
        }
      });
    }
  },
  methods: {
    selectSection(section) {
      this.activeSection = this.activeSection === section ? null : section;
      if (this.activeSection) {
        this.$nextTick(() => {
          const element = document.getElementById(section);
          if (element) {
            element.scrollIntoView({ behavior: 'smooth', block: 'start' });
          }
        });
      }
    },
    navigateToSection(section) {
      // –û–±–Ω–æ–≤–ª—è–µ–º URL —Å –Ω–æ–≤—ã–º –ø–∞—Ä–∞–º–µ—Ç—Ä–æ–º section
      this.$router.push({ 
        query: { ...this.$route.query, section } 
      });
      
      // –ü—Ä–æ–∫—Ä—É—á–∏–≤–∞–µ–º –∫ –Ω—É–∂–Ω–æ–º—É —Ä–∞–∑–¥–µ–ª—É
      this.$nextTick(() => {
        const element = document.getElementById(section);
        if (element) {
          element.scrollIntoView({ behavior: 'smooth', block: 'start' });
          // –î–æ–±–∞–≤–ª—è–µ–º –ø–æ–¥—Å–≤–µ—Ç–∫—É
          element.classList.add('highlight-section');
          setTimeout(() => {
            element.classList.remove('highlight-section');
          }, 2000);
        }
      });
    }
  }
}
</script>

